const mongoose = require("mongoose");

const User = mongoose.model("User", {
  googleID: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  firstName: {
    type: String
  },
  lastName: {
    type: String
  },
  image: {
    type: String
  }
});

module.exports.User = User;
